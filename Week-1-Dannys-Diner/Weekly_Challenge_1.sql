# 🍜 Case Study #1: Danny's Diner 
**SQL Mastery & Business Logic Challenge**

## 📌 Project Overview
After moving past the "tutorial phase" of SQL, I took on the Danny's Diner challenge to apply my skills to a real-world business scenario. This project involved analyzing a restaurant's customer behavior, membership loyalty, and sales patterns using complex relational data.

## 🚀 Key Technical Achievements
* **Advanced Joins:** Utilized `LEFT` and `INNER JOIN` to merge sales, menu, and member data without losing critical information.
* **Window Functions:** Implemented `DENSE_RANK()` to identify the sequence of customer orders and membership milestones.
* **CTE Architecture:** Used Common Table Expressions (CTEs) to ensure the code is modular, readable, and professional.
* **Data Auditing:** Created a "Master Table" (`dannys_sales`) to denormalize the schema for faster business reporting.

## 🧠 Problem Solving: Case Highlights
One of the most interesting challenges was calculating a "2x Points" promotion for the first week of membership. I solved this by using `DATE_ADD` and `BETWEEN` logic to create a dynamic promotional window for each specific member.

## 🛠️ Tools Used
* **Database:** MySQL Workbench
* **Language:** SQL (MySQL Flavor)
* **Concepts:** Aggregations, Case Statements, CTEs, Window Functions, and Schema Design.

